#include "input.h"

#include <proto/dos.h>

/*--------------------------------------------------------------------------*/

static UBYTE keys[128];

/*--------------------------------------------------------------------------*/

void InputInit(void)
{
	UWORD i = 128;

	while (0 != i)
	{
		--i;
		keys[i] = FALSE;
	}
}

/*--------------------------------------------------------------------------*/

void InputSetKeys(UWORD msgCode)
{
	keys[msgCode & 0x7f] = (msgCode < 0x80);
}

/*--------------------------------------------------------------------------*/

#define KEY_ESC 0x45

BOOL InputIsExitToOsPressed(void)
{
	return keys[KEY_ESC];
}

